    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        DefaultTableModel model = (DefaultTableModel) tblDichVu.getModel();
    int selectedRow = tblDichVu.getSelectedRow();

    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Vui lòng chọn một đơn để cập nhật!");
        return;
    }

    model.setValueAt(txtTenKH.getText(), selectedRow, 0);
    model.setValueAt(txtNgayNhan.getText(), selectedRow, 1);
    model.setValueAt(txtNgayTra.getText(), selectedRow, 2);
    JOptionPane.showMessageDialog(this, "Cập nhật đơn thành công!");
    lamMoiForm();
    }//GEN-LAST:event_btnCapNhatActionPerformed

