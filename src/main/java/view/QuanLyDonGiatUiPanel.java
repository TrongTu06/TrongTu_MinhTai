    private void btnTaoDonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTaoDonActionPerformed
        DefaultTableModel model = (DefaultTableModel) tblDichVu.getModel();

        String tenKH = txtTenKH.getText().trim();
        String ngayNhan = txtNgayNhan.getText().trim();
        String ngayTra = txtNgayTra.getText().trim();
        if (tenKH.isEmpty() || ngayNhan.isEmpty() || ngayTra.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin!");
            return;
        }

        model.addRow(new Object[]{tenKH, ngayNhan, ngayTra,});
        JOptionPane.showMessageDialog(this, "Tạo đơn giặt ủi thành công!");
        lamMoiForm();        // TODO add your handling code here:
    }//GEN-LAST:event_btnTaoDonActionPerformed

    private void btnXoaDonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaDonActionPerformed
        DefaultTableModel model = (DefaultTableModel) tblDichVu.getModel();
    int selectedRow = tblDichVu.getSelectedRow();

    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Vui lòng chọn một đơn để xóa!");
        return;
    }

    model.removeRow(selectedRow);
    JOptionPane.showMessageDialog(this, "Xóa đơn thành công!");
    lamMoiForm();        // TODO add your handling code here:
    }//GEN-LAST:event_btnXoaDonActionPerformed


